void	ft_sort_int_tab(int *tab, int size)
{
	int	temp;
	int	i;

	i = 0;
	temp = 0;
	while (i < size -1)
	{
		if (tab [i + 1] < tab [i])
		{
			temp = tab [i];
			tab [i] = tab [i +1];
			tab [i +1] = temp;
			i = 0;
		}
		else
			i++;
	}
}
